#slicing a string

name = "Napoleon Bonaparte"
slicing_names = name[0:12:4]
print(slicing_names)

#slicing if you only want the letters up until a certain index

name = "Napoleon Bonaparte"
slicing_names = name[:11]
print(slicing_names)

# slicing going backwards
name = "Napoleon Bonaparte"
slicing_names = name[::-2]
print(slicing_names)
